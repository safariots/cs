using System;

class typy_zmiennych
{
    static void Main()
    {
        Console.WriteLine("Hello World");

        int liczba = 42; // zmienna całkowita
        Console.WriteLine(liczba);

        double liczba1 = 3.13; // (floating-point)
        // zmienna zmiennoprzecinkowa
        Console.WriteLine(liczba1);

        char znak = 'M'; // zmienna znaku
        Console.WriteLine(znak);

        string tekst = "To jest zmienna srting";
        // zmienna tekstowa (str)
        Console.WriteLine(tekst);

        bool prawdafalsz = true;
        // zmienna logiczna (bolean)
        Console.WriteLine(prawdafalsz);

    }
}
